export function getTrustHistoryForAgent(agentId: string): TrustHistoryEntry[] {
  return TRUST_HISTORY.filter((h) => h.agentId === agentId).sort(
    (a, b) => new Date(a.date).getTime() - new Date(b.date).getTime(),
  );
}
